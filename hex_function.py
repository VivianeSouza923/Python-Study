'''
    hex(numero)
    é USADA PARA CONVERTER UMA NÚMERO INTEIRO EM SUA REPRESENTAÇÃO DECIMAL COMO
    UMA STRING. 
'''

numero_decimal = 255
numero_hexadecimal = hex(numero_decimal)
print(numero_hexadecimal)  # Saída: "0xff"


