'''
    5. Conversor de Celsius para Fahrenheit:
        ◦ Crie um programa que converta uma temperatura em graus Celsius para Fahrenheit. A fórmula é: F=59*C+32.
'''
temperatura_celsius = 63

temperatura_fahrenheit = 9/5*temperatura_celsius+32

print(f'A temperatura informada de {temperatura_celsius} Celsius é equivalente a {temperatura_fahrenheit} Fahrenheits.')
