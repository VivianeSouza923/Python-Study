# crio uma variável chamada codigo e atribuo um número inteiro a ela.
codigo = 65
codigo0 = 6598
'''
    a função chr() coverte um número inteiro (o código Unicode) em seu caractere 
    correspondente da tabela ASCII.

'''
# Armazeno o resultado da função aplicada à variável código em uma variável chamada caractere
caractere = chr(codigo)
caractere0 = chr(codigo0)
#visualização do resultado. 
print(caractere)  # Saída: 'A'
print(caractere0)
