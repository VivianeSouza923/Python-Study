# atribuo um número inteiro, decimal à variável numero
numero = 10
'''
    a função bin binariza um número, ou seja, transforma a base daquele número em bin-
    nário.

    o resultado da função bin aplicada à variável número será atribuída à variável re-
    presentacao_binaria
'''
representacao_binaria = bin(numero)
#visualização do resultado
print(representacao_binaria)
