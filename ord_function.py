'''
    A função ordd() é usada para obter o valor unicode de um caractere uunicode oou o valor ASCII para caractere ASCII.
    ord(caractere)

'''

valor_unicode_a = ord('a')
print(valor_unicode_a)  # Saída: 97

valor_unicode_é = ord('é')
print(valor_unicode_é)  # Saída: 233

valor_ascii_A = ord('A')
print(valor_ascii_A)  # Saída: 65
