# atribuo -10 à variável num.
num = -10
'''
    eu quero o valor absuluto de -10, portanto para isso eu utilizo a função built-in 
    (nativa do Python, ou seja, n preciso fazer nenhum import). Ela recece como 
    parametro, obviamente, o número a ser absolutizado.
    abs(numero_a_ser_absolutizado)

'''
abs_num = abs(num)
# só pra ficar bonito, printar na tela o resultado direitinho
print("O valor absoluto de", num, "é", abs_num)
