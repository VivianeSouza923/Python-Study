'''
    float(valor)
    A função acima é usada para converter um valor em ponto flutuante. Esse valor pode
    ser uma string, não só números (óbvio que são valores em string, por exemplo, '2').

'''

numero_inteiro = 42
numero_decimal = float(numero_inteiro)
print(numero_decimal)  # Saída: 42.0

texto_numero = "3.14"
numero = float(texto_numero)
print(numero)  # Saída: 3.14





