'''

8. Condicionais:
    • Crie uma variável chamada idade e atribua um valor a ela.
    • Verifique se a idade é maior que 18 e imprima "Você é maior de idade".
    • Verifique se a idade é menor que 18 e imprima "Você é menor de idade".
    • Verifique se a idade é igual a 18 e imprima "Você está completando 18 anos".

'''

idade = 12 # variável idade recebe o número inteiro 25

if idade > 18:
	print("Você é maior de idade")
elif idade < 18:
	print('Você é menor de idade')
else:
	print('Você está completando 18 anos.')